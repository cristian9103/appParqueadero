    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String usuario = txtUsuario.getText();
        String contra = String.valueOf(txtContra.getPassword());
        String rol = String.valueOf(cmbRol.getSelectedItem());
        if (!usuario.isEmpty() && !contra.isEmpty()) {
            control.crearRol(usuario, contra, rol);
            mostrarMensaje("Usuario ingresado exitosamente", "Infor", "Ingreso exitoso");
            txtUsuario.setText("");
            txtContra.setText("");
        } else {
            mostrarMensaje("Faltan datos, por favor verificar", "Error", "Falta de datos");
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    public void mostrarMensaje(String mensaje, String tipo, String titulo) {
        JOptionPane optionPane = new JOptionPane(mensaje);
        if (tipo.equals("Info")) {
            optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
        } else if (tipo.equals("Error")) {
            optionPane.setMessageType(JOptionPane.ERROR_MESSAGE);
        }
        JDialog dialog = optionPane.createDialog(titulo);
        dialog.setAlwaysOnTop(true);
        dialog.setVisible(true);
    }

