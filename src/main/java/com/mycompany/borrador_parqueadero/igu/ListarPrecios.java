    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if (tablaPrecios.getRowCount() > 0) {
            if (tablaPrecios.getSelectedRow() != -1) {
                int id = Integer.parseInt(String.valueOf(tablaPrecios.getValueAt(tablaPrecios.getSelectedRow(), 0)));

                ModificarPrecio pre = new ModificarPrecio(usr, control, id);
                pre.setVisible(true);
                pre.setLocationRelativeTo(null);
                this.dispose();
            } else {
                mostrarMensaje("No seleccionó ningún registro", "Error", "Error al editar");
            }
        } else {
            mostrarMensaje("La tabla está vacía", "Error", "Error al editar");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    public void mostrarMensaje(String mensaje, String tipo, String titulo) {
        JOptionPane optionPane = new JOptionPane(mensaje);
        if (tipo.equals("Info")) {
            optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
        } else if (tipo.equals("Error")) {
            optionPane.setMessageType(JOptionPane.ERROR_MESSAGE);
        }
        JDialog dialog = optionPane.createDialog(titulo);
        dialog.setAlwaysOnTop(true);
        dialog.setVisible(true);
    }

    private void cargarTala() {
        DefaultTableModel modeloTabla = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        String titulos[] = {"Id", "TipoVehículo", "ValorHora", "ValorDía", "ValorMensualidad"};
        modeloTabla.setColumnIdentifiers(titulos);
        List<Precio> listaPrecios = control.traerPrecios();

        if (!listaPrecios.isEmpty()) {
            for (Precio pre : listaPrecios) {
                Object[] objeto = {pre.getId(), pre.getTipoVehiculo(), pre.getValorHora(), pre.getValorDia(), pre.getValorMensual()};
                modeloTabla.addRow(objeto);
            }
        }

        tablaPrecios.setModel(modeloTabla);
    }

